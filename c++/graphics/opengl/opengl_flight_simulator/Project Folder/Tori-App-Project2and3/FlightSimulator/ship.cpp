/*
 *	These functions simply update the contents of the 
 *	ship's Matrix, mainly using glRotatef() and glTranslatef()
 */
void ship::turn(GLfloat angle, GLfloat x, GLfloat y, GLfloat z){
	glPushMatrix();
		glLoadMatrixf(this->rotateMatrix);
		glRotatef(angle, x, y, z);
		glGetFloatv(GL_MODELVIEW_MATRIX, this->rotateMatrix);
	glPopMatrix();
}

/*
 *	This function orients ship and points it towards the
 *	desired object
 */
void ship::orient_ship(torus* curr_torus){
	//Set Ships Location
	this->set_X_Pos(curr_torus->get_X_Pos() - curr_torus->get_X_At() * 3000);		//Matrix[12] = TORUS1VIEW				
	this->set_Y_Pos(curr_torus->get_Y_Pos() - curr_torus->get_Y_At() * 3000);		//Matrix[13] = TORUS1VIEW
	this->set_Z_Pos(curr_torus->get_Z_Pos() - curr_torus->get_Z_At() * 3000);		//Matrix[14] = TORUS1VIEW
	//	OBTAIN THE VECTORS
	GLfloat v_vector[3];	//	vector to target			V
	v_vector[0] = curr_torus->get_X_Pos() - this->get_X_Pos();
	v_vector[1] = curr_torus->get_Y_Pos() - this->get_Y_Pos();
	v_vector[2] = curr_torus->get_Z_Pos() - this->get_Z_Pos();
	GLfloat v_magnitude = sqrt(v_vector[0]*v_vector[0] + v_vector[1]*v_vector[1] + v_vector[2]*v_vector[2]);
	v_vector[0] /= v_magnitude;
	v_vector[1] /= v_magnitude;
	v_vector[2] /= v_magnitude;
	GLfloat u_vector[3];	//	At vector of torpedo		U
	u_vector[0] = this->get_X_At();
	u_vector[1] = this->get_Y_At();
	u_vector[2] = this->get_Z_At();
	GLfloat u_magnitude = sqrt(u_vector[0]*u_vector[0] + u_vector[1]*u_vector[1] + u_vector[2]*u_vector[2]);
	u_vector[0] /= u_magnitude;
	u_vector[1] /= u_magnitude;
	u_vector[2] /= u_magnitude;
	GLfloat normal[3];
	normal[0] = v_vector[1]*u_vector[2] - u_vector[1]*v_vector[2];
	normal[1] = v_vector[2]*u_vector[0] - v_vector[0]*u_vector[2];
	normal[2] = v_vector[0]*u_vector[1] - u_vector[0]*v_vector[1];
	GLfloat n_magnitude = sqrt(normal[0]*normal[0] + normal[1]*normal[1] + normal[2]*normal[2]);
	normal[0] /= n_magnitude;
	normal[1] /= n_magnitude;
	normal[2] /= n_magnitude;
	//	NEXT WE NEED TO DETERMINE HOW MUCH TO ROTATE THE MISSLE TO GET IT TO POINT TO TARGET
	GLfloat u_dot_v =	(u_vector[0]*v_vector[0]) + 
						(u_vector[1]*v_vector[1]) + 
						(u_vector[2]*v_vector[2]);
	GLfloat angle = acos(u_dot_v);
	angle *= 180/PI;
	this->turn((angle), normal[0], (normal[1]), normal[2]);
}

